import turtle

wn = turtle.Screen()
tim = turtle.Turtle()
terry = turtle.Turtle()
tanya = turtle.Turtle()
taylor = turtle.Turtle()
tony = turtle.Turtle()

for a in range(4):
    tim.forward(12.5)
    tim.left(90)
    terry.forward(25)
    terry.left(90)
    tanya.forward(50)
    tanya.left(90)
    taylor.forward(100)
    taylor.left(90)
    tony.forward(200)
    tony.left(90)

wn.mainloop()
